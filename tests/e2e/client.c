#include <sys/socket.h>
#include <assert.h>
#include <netinet/in.h>
#include <string.h>
#include <unistd.h>

const int PORT = 1026;

// dummy packet
const uint8_t send_buf[] = {
        0x45, 0x00, 0x00, 0x40, 0x00, 0x00, 0x40, 0x00, 0x40, 0x06, 0xfb, 0xe7, 0x0a, 0x08, 0x00, 0x02,
        0x5d, 0xb8, 0xd7, 0x0e, 0xc7, 0x1a, 0x00, 0x50, 0x3d, 0xa9, 0xd9, 0x17, 0x00, 0x00, 0x00, 0x00,
        0xb0, 0x02, 0xff, 0xff, 0xe9, 0xd5, 0x00, 0x00, 0x02, 0x04, 0x05, 0xb4, 0x01, 0x03, 0x03, 0x06,
        0x01, 0x01, 0x08, 0x0a, 0x42, 0x80, 0xed, 0xa9, 0x00, 0x00, 0x00, 0x00, 0x04, 0x02, 0x00, 0x00
};

int main() {
    int client_fd = socket(AF_INET, SOCK_DGRAM, 0);
    assert(client_fd != -1);

    struct sockaddr_in server_addr;
    memset(&server_addr, 0, sizeof(server_addr));

    server_addr.sin_family = AF_INET;
    server_addr.sin_port = htons(PORT);
    server_addr.sin_addr.s_addr = INADDR_ANY;

    int n = sendto(client_fd, send_buf, sizeof(send_buf), 0, (const struct sockaddr *) &server_addr,
                   sizeof(struct sockaddr_in));
    assert(n == sizeof(send_buf));

    close(client_fd);
    return 0;
}